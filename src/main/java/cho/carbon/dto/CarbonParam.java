package cho.carbon.dto;

/**
 * carbon 参数接口
 * 
 * @author lhb
 *
 */
public class CarbonParam {
	private String funcGroupContext;
	private String recordCode;
	private String opsComplexus;
	private String fGRootRecord;
	private String modelConJunction;
	private String fGRecordComplexus;
	
	
	public String getFuncGroupContext() {
		return funcGroupContext;
	}
	public void setFuncGroupContext(String funcGroupContext) {
		this.funcGroupContext = funcGroupContext;
	}
	public String getRecordCode() {
		return recordCode;
	}
	public void setRecordCode(String recordCode) {
		this.recordCode = recordCode;
	}
	public String getOpsComplexus() {
		return opsComplexus;
	}
	public void setOpsComplexus(String opsComplexus) {
		this.opsComplexus = opsComplexus;
	}
	public String getfGRootRecord() {
		return fGRootRecord;
	}
	public void setfGRootRecord(String fGRootRecord) {
		this.fGRootRecord = fGRootRecord;
	}
	public String getModelConJunction() {
		return modelConJunction;
	}
	public void setModelConJunction(String modelConJunction) {
		this.modelConJunction = modelConJunction;
	}
	public String getfGRecordComplexus() {
		return fGRecordComplexus;
	}
	public void setfGRecordComplexus(String fGRecordComplexus) {
		this.fGRecordComplexus = fGRecordComplexus;
	}
	
}
